    ) AS foundation(FOUNDATION)
),
daily_transfers AS (
  SELECT
    DATE(BLOCK_TIMESTAMP) as TIMESTAMP,
    SUM(
      CASE
        WHEN FROM_ADDRESS IN (
          SELECT
            FOUNDATION
          FROM
            foundation_list
        ) THEN - RAW_AMOUNT / POW(10, 18)
        WHEN TO_ADDRESS IN (
          SELECT
            FOUNDATION
          FROM
            foundation_list
        ) THEN RAW_AMOUNT / POW(10, 18)
        ELSE 0
      END
    ) AS daily_balance_change
  FROM
    ethereum.core.ez_token_transfers
  WHERE
    CONTRACT_ADDRESS = LOWER('0x4d224452801ACEd8B2F0aebE155379bb5D594381')
    AND (
      FROM_ADDRESS in (
        SELECT
          FOUNDATION
        FROM
          foundation_list
      )
      OR TO_ADDRESS in (
        SELECT
          FOUNDATION
        FROM
          foundation_list
      )
    )
    AND NOT (
      FROM_ADDRESS in (
        SELECT
          FOUNDATION
        FROM
          foundation_list
      )
      AND TO_ADDRESS in (
        SELECT
          FOUNDATION
        FROM
          foundation_list
      )
    )
  GROUP BY
    TIMESTAMP
)
SELECT
  DISTINCT TIMESTAMP AS DATE,
  1000000000 - SUM(daily_balance_change) OVER (
    ORDER BY
      TIMESTAMP
  ) AS CIRCULATING,
  (
    EXTRACT(
      EPOCH
      FROM
        TIMESTAMP
    ) - 1647475200
  ) / 86400 AS DAYS,
  CASE
    WHEN DAYS BETWEEN 0
    AND 183 THEN 277500000 + DAYS * 377390.71
    WHEN DAYS BETWEEN 183
    AND 365 THEN 346562500 + (DAYS -183) * 379464.29
    WHEN DAYS BETWEEN 365
    AND 548 THEN 415625000 + (DAYS -365) * 438659.17
    WHEN DAYS > 548 THEN 520899627.24
  END AS PLANNED
FROM
  daily_transfers
ORDER BY
  TIMESTAMP;
